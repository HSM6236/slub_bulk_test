#!/usr/bin/env python3
"""
SLUB Bulk Benchmark Result Summarizer

This script parses benchmark log files generated by slub_bulk_bench
and summarizes the performance difference between two configurations,
typically labeled as "before" and "after".

Functionality:
- Scans a result directory for matching log file pairs:
    before-*.log and after-*.log
- Extracts the following fields from each valid benchmark line:
    - object size (obj)
    - batch size (batch)
    - per-object latency in nanoseconds (ns_per_obj)
- Computes, for each before/after pair:
    - mean latency
    - standard deviation
    - percentage change (delta)
- Optionally reads metadata from meta.txt in the same directory
- Prints a formatted summary for all matched benchmark cases

Input:
- A directory path passed as a command-line argument
- The directory should contain:
    - before-*.log
    - after-*.log
- Optional:
    - meta.txt

Output:
- A human-readable benchmark summary printed to stdout

Error handling:
- Reports missing files, malformed data, mismatched object sizes,
  mismatched batch sizes, and permission issues
- Exits with an error if no valid before/after log pairs are found

Typical usage:
    python3 summarize.py RESULTS_DIR
"""
import math
import re
import sys
from pathlib import Path

LINE_RE = re.compile(
    r"obj=(?P<obj>\d+)\s+batch=(?P<batch>\d+).*?ns_per_obj=(?P<ns>\d+)"
)

def parse_ns_per_obj(path: Path):
    vals = []
    batch_seen = None
    obj_seen = None

    try:
        lines = path.read_text().splitlines()
    except PermissionError:
        raise RuntimeError(
            f"cannot read {path}: permission denied; "
            f"try 'sudo chmod a+r {path}' or run the summarizer with sudo"
        )
    except FileNotFoundError:
        raise RuntimeError(f"file not found: {path}")

    for line in lines:
        m = LINE_RE.search(line)
        if not m:
            continue
        obj = int(m.group("obj"))
        batch = int(m.group("batch"))
        ns = int(m.group("ns"))
        vals.append(ns)
        obj_seen = obj
        batch_seen = batch

    if not vals:
        raise RuntimeError(f"no ns_per_obj found in {path}")

    return obj_seen, batch_seen, vals

def mean(xs):
    return sum(xs) / len(xs)

def stdev(xs):
    if len(xs) < 2:
        return 0.0
    m = mean(xs)
    return math.sqrt(sum((x - m) ** 2 for x in xs) / (len(xs) - 1))

def fmt(v):
    return f"{v:.2f}"

def summarize_pair(before_path: Path, after_path: Path):
    b_obj, b_batch, b_vals = parse_ns_per_obj(before_path)
    a_obj, a_batch, a_vals = parse_ns_per_obj(after_path)

    if b_obj != a_obj:
        raise RuntimeError(
            f"obj_size mismatch: {before_path.name} has obj={b_obj}, "
            f"but {after_path.name} has obj={a_obj}"
        )
    if b_batch != a_batch:
        raise RuntimeError(
            f"batch mismatch: {before_path.name} has batch={b_batch}, "
            f"but {after_path.name} has batch={a_batch}"
        )

    b_mean = mean(b_vals)
    b_std = stdev(b_vals)
    a_mean = mean(a_vals)
    a_std = stdev(a_vals)

    delta = (a_mean - b_mean) / b_mean * 100.0

    return {
        "obj": b_obj,
        "batch": b_batch,
        "before_mean": b_mean,
        "before_std": b_std,
        "after_mean": a_mean,
        "after_std": a_std,
        "delta": delta,
        "before_runs": len(b_vals),
        "after_runs": len(a_vals),
        "before_file": before_path.name,
        "after_file": after_path.name,
    }

def read_meta(meta_path: Path):
    if meta_path.exists():
        try:
            return meta_path.read_text().rstrip()
        except PermissionError:
            return f"[WARN] cannot read {meta_path}: permission denied"
    return ""

def discover_pairs(d: Path):
    pairs = []

    for before_path in sorted(d.glob("before-*.log")):
        suffix = before_path.name[len("before-"):]
        after_path = d / f"after-{suffix}"
        if after_path.exists():
            pairs.append((before_path, after_path))
        else:
            print(
                f"[WARN] missing matching file for {before_path.name}: "
                f"expected {after_path.name}",
                file=sys.stderr,
            )

    return pairs

def main():
    if len(sys.argv) != 2:
        print(f"usage: {sys.argv[0]} RESULTS_DIR", file=sys.stderr)
        sys.exit(1)

    d = Path(sys.argv[1])

    if not d.exists():
        print(f"error: directory does not exist: {d}", file=sys.stderr)
        sys.exit(1)
    if not d.is_dir():
        print(f"error: not a directory: {d}", file=sys.stderr)
        sys.exit(1)

    meta = read_meta(d / "meta.txt")
    file_pairs = discover_pairs(d)

    if not file_pairs:
        print("error: no before-*.log / after-*.log pairs found", file=sys.stderr)
        sys.exit(1)

    cases = []
    for before_path, after_path in file_pairs:
        try:
            cases.append(summarize_pair(before_path, after_path))
        except RuntimeError as e:
            print(f"error: {e}", file=sys.stderr)
            sys.exit(1)

    cases.sort(key=lambda c: c["obj"])

    print("Benchmark:")
    if meta:
        print(meta)
        if not meta.endswith("\n"):
            print()

    for c in cases:
        print(
            f"slub_bulk_bench, obj_size={c['obj']}, batch={c['batch']} "
            f"(= objs_per_slab), total=256MB:"
        )
        print(
            f"before: {fmt(c['before_mean'])} +- {fmt(c['before_std'])} ns/object"
        )
        print(
            f"after: {fmt(c['after_mean'])} +- {fmt(c['after_std'])} ns/object"
        )
        print(f"delta: {c['delta']:+.1f}%")
        print()

if __name__ == "__main__":
    main()
